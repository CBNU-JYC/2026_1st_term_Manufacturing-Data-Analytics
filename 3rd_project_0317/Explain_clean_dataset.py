# [주석 1]
# pandas를 쓰는 이유:
# - CSV 파일을 쉽게 읽고 쓸 수 있습니다.
# - 결측치, 중복, 조건 필터링 같은 전처리 기능이 풍부합니다.
# - 데이터 분석과 머신러닝 실습의 사실상 표준 도구입니다.
#
# [주석 2]
# pd.read_csv('labeled_data.csv')의 의미:
# - 현재 작업 폴더에 있는 labeled_data.csv 파일을 읽습니다.
# - 파일 경로가 다르면 FileNotFoundError가 발생할 수 있습니다.
#
# [주석 3]
# 전처리 전에 행 개수를 확인하는 이유:
# - 나중에 몇 건이 제거되었는지 비교할 수 있습니다.
# - 잘못된 필터링으로 데이터가 과도하게 사라졌는지 점검할 수 있습니다.
#
# [주석 4]
# 복사본을 사용하는 이유:
# - 원본 보존
# - 디버깅 편의성
# - 전처리 전/후 비교 가능
#
# [주석 5]
# 데이터 품질 5관점:
# - 유일성: 중복 여부
# - 완전성: 결측 여부
# - 유효성: 값 형식과 범위의 적절성
# - 일관성: 서로 모순되는 값 존재 여부
# - 정확성: 실제 현상을 잘 반영하는지
#
# [주석 6]
# duplicated의 subset 의미:
# - 모든 컬럼이 아니라, "중복 판단의 핵심 기준"이 되는 컬럼만 사용합니다.
# - 여기서는 시간, 설비, 파츠 조합이 같으면 같은 생산 기록으로 봅니다.
#
# [주석 7]
# 중복 제거 전략은 상황에 따라 달라질 수 있습니다.
# - 첫 행 유지
# - 마지막 행 유지
# - 모두 제거
# - 평균/집계 처리
# 여기서는 가장 단순한 첫 행 유지 전략을 사용했습니다.
#
# [주석 8]
# 완전성 검사는 특히 중요합니다.
# 핵심 센서값이 비어 있으면 공정 상태를 올바르게 판단하기 어렵기 때문입니다.
#
# [주석 9]
# critical_cols는 도메인 지식이 반영된 예시입니다.
# 즉, 어떤 컬럼이 "핵심"인지는 산업/문제에 따라 달라집니다.
#
# [주석 10]
# isnull().any(axis=1).sum() 패턴은
# "특정 컬럼들 중 하나라도 비어 있는 행이 몇 개인가?"를 셀 때 자주 씁니다.
#
# [주석 11]
# 결측치 처리 방법은 여러 가지가 있습니다.
# - 행 제거
# - 평균 대체
# - 중앙값 대체
# - 보간법
# - 예측 모델 기반 대체
# 이 코드는 학습용 예제로서 가장 직관적인 "행 제거" 전략을 택했습니다.
#
# [주석 12]
# 유효성은 데이터 형식과 물리 법칙을 함께 봅니다.
# 예: 시간, 길이, 온도, 수량이 음수가 될 수 있는지 여부
#
# [주석 13]
# 불리언 조건식:
# - True면 문제 있음
# - False면 정상
# pandas는 이런 조건식을 이용해 데이터를 매우 빠르게 걸러낼 수 있습니다.
#
# [주석 14]
# ~ 기호는 불리언 반전(not)입니다.
# 즉, invalid_time이 True인 행을 제외하고 False인 정상 행만 남깁니다.
#
# [주석 15]
# 일관성 검사는 "한 컬럼만 보면 정상인데, 여러 컬럼을 함께 보면 모순"인 경우를 찾습니다.
# 실무에서 놓치기 쉬우나 매우 중요한 검사입니다.
#
# [주석 16]
# 라벨과 사유의 충돌 예:
# - PassOrFail == 'Y'  -> 양품
# - Reason != None     -> 불량 원인 존재
# 이 둘이 동시에 참이면 기록 오류 가능성이 큽니다.
#
# [주석 17]
# Injection_Time >= Cycle_Time 이 왜 문제인가:
# - Cycle_Time은 전체 공정 시간
# - Injection_Time은 그 안의 일부 단계 시간
# 부분 시간이 전체 시간보다 길 수는 없습니다.
#
# [주석 18]
# 여러 필터를 순차 적용하면 데이터가 단계적으로 줄어듭니다.
# 따라서 각 단계마다 몇 건이 줄었는지 기록하면 원인 분석이 쉬워집니다.
#
# [주석 19]
# 정확성은 흔히 "도메인 지식"이 많이 들어가는 단계입니다.
# 즉, 현장 지식이나 장비 특성을 알아야 규칙을 잘 만들 수 있습니다.
#
# [주석 20]
# 온도 임계값은 통계가 아니라 현장 상식 기반 규칙입니다.
# 이런 규칙 기반 필터는 설명 가능성이 높다는 장점이 있습니다.
#
# [주석 21]
# 이상치 처리 방식도 다양합니다.
# - 제거
# - 상한/하한 절단
# - 치환
# - 별도 라벨링
# 이 코드는 가장 단순한 제거 방식을 사용합니다.
#
# [주석 22]
# 전처리 결과 요약은 보고서, 발표, 과제 제출 때 매우 중요합니다.
# 단순히 "정제했다"가 아니라 "몇 건이 어떻게 바뀌었는지"를 보여줘야 합니다.
#
# [주석 23]
# to_csv(..., index=False)의 의미:
# - pandas의 행 번호(index)를 파일에 추가로 저장하지 않겠다는 뜻입니다.
# - 보통 CSV 결과물에는 불필요한 인덱스 컬럼이 생기지 않도록 index=False를 사용합니다.
