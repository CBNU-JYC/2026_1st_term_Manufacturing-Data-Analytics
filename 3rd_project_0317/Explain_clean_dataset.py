"""
Explain_clean_dataset.py

이 파일은 clean_dataset.py의 "해설용 사본"입니다.
실행 로직은 원본과 동일한 흐름을 유지하되, 코드 해석이 쉽도록
설명 주석과 각주 형식의 해설을 추가했습니다.

[각주 읽는 법]
- [주석 1], [주석 2]처럼 번호를 붙였습니다.
- 각 번호에 대한 자세한 설명은 파일 맨 아래 "각주 모음"에 정리했습니다.
- 주석은 실행 결과에 영향을 주지 않습니다.
"""

import pandas as pd

# [주석 1]
# pandas는 표 형태 데이터(CSV, 엑셀, 테이블)를 다룰 때 가장 많이 쓰는 라이브러리입니다.
# 여기서는 CSV 읽기, 결측치 검사, 중복 제거, 조건 필터링, CSV 저장 역할을 맡습니다.


# 1. 실제 데이터 로드
# [주석 2]
# 'labeled_data.csv' 파일을 메모리로 불러와 DataFrame 형태로 저장합니다.
# DataFrame은 엑셀 시트처럼 행(row)과 열(column)로 구성된 데이터 구조입니다.
df = pd.read_csv('labeled_data.csv')

# [주석 3]
# len(df)는 전체 행 개수, 즉 전체 레코드 수를 의미합니다.
# 전처리 전 원본 데이터 크기를 먼저 확인하는 습관은 매우 중요합니다.
print(f"[최초 데이터 로드] 총 레코드 수: {len(df)}건\n")


# [주석 4]
# 원본 df는 보존하고, 정제 작업은 복사본인 df_clean에서 진행합니다.
# 이렇게 하면 원본과 정제본을 비교하기 쉽고, 실수로 원본을 훼손하는 것도 막을 수 있습니다.
df_clean = df.copy()


# ==========================================
# 1. 유일성 (Uniqueness) 검증
# ==========================================
# [주석 5]
# 유일성이란 "같은 데이터가 중복으로 들어있지 않은가?"를 확인하는 품질 기준입니다.
#
# 실습 포인트:
# 사출성형기의 특성상 좌/우 파츠(예: RH, LH)가 동시에 생산될 수 있으므로
# 동일 시간(TimeStamp), 동일 설비(EQUIP_CD), 동일 파츠(PART_NAME)가
# 완전히 같은 기록은 중복 로깅된 오류로 볼 수 있습니다.
print("[1. 유일성 평가]")

# [주석 6]
# duplicated(..., keep=False)는 지정한 컬럼 조합이 중복이면
# 첫 번째 행까지 포함해서 모두 True로 표시합니다.
# 여기서는 중복 여부를 "찾는 것"이 목적이므로 keep=False를 사용합니다.
duplicates = df_clean.duplicated(
    subset=['TimeStamp', 'EQUIP_CD', 'PART_NAME'],
    keep=False,
)
print(f" - 중복 기록 데이터 수: {duplicates.sum()}건 발견")

# [주석 7]
# 실제 제거 단계입니다.
# drop_duplicates(..., keep='first')는 중복된 행들 중 첫 번째만 남기고 나머지를 제거합니다.
df_clean = df_clean.drop_duplicates(
    subset=['TimeStamp', 'EQUIP_CD', 'PART_NAME'],
    keep='first',
)
print("중복 데이터 제거 완료\n")


# ==========================================
# 2. 완전성 (Completeness) 검증
# ==========================================
# [주석 8]
# 완전성이란 "필수 정보가 빠지지 않았는가?"를 확인하는 품질 기준입니다.
#
# 실습 포인트:
# 공정의 핵심이 되는 센서값(사출시간, 사이클시간, 배럴온도 등)에
# 결측치(NaN/Null)가 있는지 확인합니다.
print("[2. 완전성 평가]")

# [주석 9]
# 이 리스트는 "반드시 값이 있어야 한다"고 판단한 핵심 컬럼들입니다.
# 즉, 이 컬럼들에 빈칸이 있으면 해당 행은 공정 해석에 불리하다고 본 것입니다.
critical_cols = ['Injection_Time', 'Cycle_Time', 'Barrel_Temperature_1']

# [주석 10]
# isnull()은 결측치 여부를 True/False로 반환합니다.
# any(axis=1)은 한 행 안에서 critical_cols 중 하나라도 결측이면 True입니다.
# sum()을 하면 True 개수를 세므로 "문제 행 수"를 구할 수 있습니다.
missing_count = df_clean[critical_cols].isnull().any(axis=1).sum()
print(f" - 핵심 센서 결측치(NaN) 수: {missing_count}건 발견")

# [주석 11]
# dropna(subset=...)는 지정한 핵심 컬럼에 결측치가 있는 행만 제거합니다.
# 여기서는 결측값을 보정하지 않고, 해당 공정 사이클 자체를 제외하는 전략을 택했습니다.
df_clean = df_clean.dropna(subset=critical_cols)
print("결측치 포함 사이클 제거 완료\n")


# ==========================================
# 3. 유효성 (Validity) 검증
# ==========================================
# [주석 12]
# 유효성이란 "값이 형식상/물리적으로 말이 되는가?"를 확인하는 기준입니다.
#
# 실습 포인트:
# 사출 시간 및 사이클 시간은 물리적으로 0이나 음수가 될 수 없습니다.
print("[3. 유효성 평가]")

# [주석 13]
# 조건식 두 개를 |(OR)로 묶었습니다.
# Cycle_Time <= 0 이거나 Injection_Time <= 0 이면 비정상 데이터로 간주합니다.
invalid_time = (df_clean['Cycle_Time'] <= 0) | (df_clean['Injection_Time'] <= 0)
print(f" - 유효하지 않은 시간(<=0) 데이터 수: {invalid_time.sum()}건 발견")

# [주석 14]
# ~invalid_time은 "비정상이 아닌 행만 남긴다"는 뜻입니다.
# pandas에서는 불리언 마스크를 이용해 조건에 맞는 행만 선택할 수 있습니다.
df_clean = df_clean[~invalid_time]
print("유효성 위배 데이터 제거 완료\n")


# ==========================================
# 4. 일관성 (Consistency) 검증
# ==========================================
# [주석 15]
# 일관성이란 "서로 관련된 값들 사이에 논리적 모순이 없는가?"를 확인하는 기준입니다.
#
# 실습 포인트 1:
# 양불 판정(PassOrFail)이 'Y(양품)'인데, 불량 사유(Reason)가 적혀 있으면 모순입니다.
#
# 실습 포인트 2:
# 사출 시간(Injection_Time)이 전체 사이클 시간(Cycle_Time)보다 길면 모순입니다.
print("[4. 일관성 평가]")

# [주석 16]
# '양품(Y)'인데 동시에 불량 사유가 존재하면 논리적으로 충돌합니다.
# Reason이 비어 있지 않고, 문자열 'None'도 아닐 때만 실제 사유가 있다고 봅니다.
inconsistent_label = (
    (df_clean['PassOrFail'] == 'Y')
    & (df_clean['Reason'].notna() & (df_clean['Reason'] != 'None'))
)

# [주석 17]
# 전체 사이클 시간보다 사출 시간이 길 수는 없으므로 모순 데이터로 판단합니다.
inconsistent_time = df_clean['Injection_Time'] >= df_clean['Cycle_Time']

print(f" - 양불/사유 논리적 모순 수: {inconsistent_label.sum()}건 발견")
print(f" - 공정 시간 논리적 모순(사출>=사이클) 수: {inconsistent_time.sum()}건 발견")

# [주석 18]
# 두 종류의 모순 데이터를 순차적으로 제거합니다.
df_clean = df_clean[~inconsistent_label]
df_clean = df_clean[~inconsistent_time]
print("일관성 위배 데이터 제거 완료\n")


# ==========================================
# 5. 정확성 (Accuracy) 검증
# ==========================================
# [주석 19]
# 정확성이란 "측정값이 현실 세계에서 상식적이고 신뢰할 만한 범위에 있는가?"를 보는 기준입니다.
#
# 실습 포인트:
# 본 데이터셋의 배럴 온도(Barrel_Temperature)는 통상 200~300도 사이에서 운용됩니다.
# 센서 단선이나 오작동으로 인해 50도 미만이거나 400도 초과인
# 상식 밖의 데이터(물리적 오류)를 걸러냅니다.
print("[5. 정확성 평가]")

# [주석 20]
# 온도 범위 기반 이상치 탐지입니다.
# 여기서는 도메인 지식을 반영해 임계값(50, 400)을 직접 규칙으로 설정했습니다.
inaccurate_temp = (
    (df_clean['Barrel_Temperature_1'] < 50)
    | (df_clean['Barrel_Temperature_1'] > 400)
)
print(f" - 물리적으로 부정확한 배럴 온도 수: {inaccurate_temp.sum()}건 발견")

# [주석 21]
# 이상 온도를 가진 행을 제거해 데이터 신뢰도를 높입니다.
df_clean = df_clean[~inaccurate_temp]
print("정확성 위배 데이터 제거 완료\n")


# ==========================================
# [최종 결과] 정제된 고품질 데이터셋 저장
# ==========================================
# [주석 22]
# 전처리 전후 데이터 건수를 비교하면,
# 각 품질 규칙이 데이터에 얼마나 영향을 미쳤는지 빠르게 파악할 수 있습니다.
print("=" * 60)
print("[최종 확보된 고품질 데이터셋 (Golden Dataset)]")
print(f" - 최초 원본 데이터: {len(df)}건")
print(f" - 최종 확보 데이터: {len(df_clean)}건")
print("=" * 60)

# [주석 23]
# 정제된 결과를 새 파일로 저장합니다.
# 원본을 덮어쓰지 않고 별도 파일로 저장하는 것은 안전한 실무 습관입니다.
df_clean.to_csv('labeled_data_clean.csv', index=False)
print("파일이 'labeled_data_clean.csv'로 성공적으로 저장되었습니다.")


