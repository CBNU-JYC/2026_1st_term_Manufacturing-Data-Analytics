"""
z_explanation2_clean_uci-secom_dataset.py

이 파일은 clean_uci-secom_dataset.py를
"발표용 / 과제 설명용" 스타일로 다시 풀어쓴 해설 버전입니다.

특징:
- 원본 코드와 같은 순서로 동작합니다.
- 각 코드 블록 위에 "무엇을 하는지", "왜 하는지"를 문장으로 설명합니다.
- 함수 내부도 한 줄씩 따라가기 쉽게 자세한 주석을 붙였습니다.

즉, 이 파일은
"코드가 실행되는 파일"이면서 동시에
"코드를 설명하는 문서" 역할도 하도록 만든 버전입니다.
"""

import pandas as pd
import numpy as np
from pathlib import Path


# ==========================================================
# 0. 파일 경로 준비
# ==========================================================
# 이 부분은 "데이터 파일이 어디 있는지"를 정확히 지정하는 단계입니다.
#
# 초보자가 자주 겪는 오류 중 하나가 FileNotFoundError인데,
# 그 이유는 파이썬이 CSV 파일을 현재 터미널 위치 기준으로 찾기 때문입니다.
# 그래서 여기서는 "이 파이썬 파일이 있는 폴더"를 기준으로
# 입력 파일과 출력 파일의 위치를 안전하게 정합니다.

BASE_DIR = Path(__file__).resolve().parent
INPUT_PATH = BASE_DIR / "uci-secom.csv"
OUTPUT_PATH = BASE_DIR / "cleaned_secom_data.csv"


# ==========================================================
# 1. 데이터 불러오기
# ==========================================================
# 이제 실제 CSV 데이터를 불러옵니다.
# pandas의 read_csv 함수는 CSV 파일을 표 형태의 DataFrame으로 읽어옵니다.
#
# DataFrame은 엑셀 표처럼 생각하면 이해하기 쉽습니다.
# - 행(row): 하나의 관측값
# - 열(column): 하나의 변수 또는 센서 항목

df = pd.read_csv(INPUT_PATH)

# shape는 (행 개수, 열 개수)를 보여줍니다.
# 전처리 전 데이터 규모를 먼저 확인해 두면
# 나중에 얼마나 정제되었는지 비교하기 좋습니다.
print(f"원본 데이터 크기: {df.shape}")


# ==========================================================
# 2. 유일성(Unqueness) 확보: 중복 데이터 제거
# ==========================================================
# 데이터 품질의 첫 번째 관점은 "유일성"입니다.
# 즉, 완전히 같은 데이터가 여러 번 들어 있지 않은지 확인하는 것입니다.
#
# 동일한 행이 반복되면
# - 특정 패턴이 실제보다 더 많이 나타난 것처럼 보일 수 있고
# - 통계량이나 모델 학습 결과가 왜곡될 수 있습니다.

before_rows = len(df)

# drop_duplicates()는 모든 컬럼 값이 완전히 같은 행을 중복으로 보고 제거합니다.
# 기본적으로 첫 번째 행은 남기고, 뒤에 반복된 행만 제거합니다.
df = df.drop_duplicates()

after_rows = len(df)
print(f"1. 유일성 확보: 중복 행 {before_rows - after_rows}건 제거")


# ==========================================================
# 3. 완전성(Completeness) 확보: 결측치 처리
# ==========================================================
# 두 번째 관점은 "완전성"입니다.
# 완전성은 데이터가 얼마나 빠짐없이 채워져 있는지와 관련됩니다.
#
# 센서 데이터에서는 일부 컬럼이 거의 비어 있을 수 있습니다.
# 이런 컬럼은 정보가 부족해 분석에 방해가 될 수 있으므로 먼저 제거합니다.
# 이후, 남은 소수의 결측치는 중앙값으로 채워 데이터 손실을 줄입니다.

# 결측 비율 50%를 기준값으로 둡니다.
# 즉, 절반보다 많이 비어 있으면 제거 대상으로 판단합니다.
nan_threshold = 0.5

# isnull()은 각 셀이 비어 있는지(True/False) 표시합니다.
# mean()을 적용하면 True는 1, False는 0처럼 계산되어
# 각 컬럼의 결측 비율을 구할 수 있습니다.
null_counts = df.isnull().mean()

# 결측 비율이 50%를 초과하는 컬럼 이름만 따로 추출합니다.
cols_to_drop = null_counts[null_counts > nan_threshold].index

# 결측이 너무 많은 컬럼은 통째로 제거합니다.
df = df.drop(columns=cols_to_drop)

# 남은 결측치는 중앙값으로 채웁니다.
# 제조 데이터는 이상치가 있을 수 있기 때문에 평균보다 중앙값이 더 안정적일 수 있습니다.
# numeric_only=True는 숫자형 컬럼에 대해서만 중앙값을 계산하라는 뜻입니다.
df = df.fillna(df.median(numeric_only=True))

print(f"2. 완전성 확보: 결측치 과다 컬럼 {len(cols_to_drop)}개 삭제 및 나머지 중앙값 보간")


# ==========================================================
# 4. 유효성(Validity) 확보: 상수 컬럼 제거
# ==========================================================
# 세 번째 관점은 "유효성"입니다.
# 여기서는 특히 "분석에 의미가 있는 값인가?"라는 관점에서 봅니다.
#
# 어떤 컬럼의 값이 모든 행에서 똑같다면,
# 그 컬럼은 데이터를 구분하는 데 전혀 도움을 주지 못합니다.
# 예를 들어 모든 샘플이 같은 값을 가진 센서라면,
# 그 센서는 상태 변화나 불량 여부를 설명해 주지 못합니다.

# nunique()는 각 컬럼의 서로 다른 값 개수를 셉니다.
# 고유값 개수가 1 이하이면 상수 컬럼으로 간주합니다.
constant_cols = [col for col in df.columns if df[col].nunique() <= 1]

# 분석 가치가 없는 상수 컬럼을 제거합니다.
df = df.drop(columns=constant_cols)

print(f"3. 유효성 확보: 변동성 없는 상수 센서 {len(constant_cols)}개 제거")


# ==========================================================
# 5. 일관성(Consistency) 확보: 라벨 형식 통일
# ==========================================================
# 네 번째 관점은 "일관성"입니다.
# 데이터의 형식이 통일되어 있어야 이후 분석과 모델링이 쉬워집니다.
#
# 이 데이터셋의 Pass/Fail 라벨은 -1, 1로 구성되어 있습니다.
# 하지만 많은 머신러닝 예제와 분류 문제에서는 0, 1 형식을 더 자주 사용합니다.
# 따라서 여기서는 양품/불량 라벨을 0/1 체계로 통일합니다.

# -1은 0으로 바꾸고, 1은 그대로 둡니다.
# 그 다음 astype(int)로 자료형을 정수형으로 고정합니다.
df["Pass/Fail"] = df["Pass/Fail"].replace(-1, 0).astype(int)

print("4. 일관성 확보: 라벨 포맷 변경 (-1, 1) -> (0, 1)")


# ==========================================================
# 6. 정확성(Accuracy) 확보: 시간 형식 변환
# ==========================================================
# 다섯 번째 관점은 "정확성"입니다.
# 여기서는 먼저 Time 컬럼을 올바른 시간 타입으로 변환합니다.
#
# CSV에서 날짜/시간은 보통 문자열로 읽힙니다.
# 그런데 문자열 상태로 두면 시간 정렬, 시간 차 계산, 시계열 분석이 불편합니다.
# 그래서 pandas의 datetime 형식으로 변환합니다.

df["Time"] = pd.to_datetime(df["Time"])


# ==========================================================
# 7. 정확성(Accuracy) 확보: IQR 기반 이상치 보정 함수 정의
# ==========================================================
# 센서 데이터에는 비정상적으로 너무 크거나 너무 작은 값이 포함될 수 있습니다.
# 이런 값을 이상치(outlier)라고 합니다.
#
# 이상치를 처리하는 방법은 여러 가지가 있지만,
# 여기서는 IQR(사분위 범위) 기반 클리핑(clipping) 방식을 사용합니다.
# 이 방법은 값을 삭제하지 않고, 지나치게 벗어난 값만 경계선까지 잘라주는 방식입니다.

def handle_outliers(series):
    """
    하나의 숫자형 컬럼(Series)에 대해 IQR 기준으로 이상치를 보정하는 함수.

    입력:
    - series: pandas Series, 예를 들어 하나의 센서 컬럼 전체

    출력:
    - 이상치가 경계값으로 조정된 Series
    """

    # 1사분위수(Q1): 데이터의 하위 25% 지점 값
    Q1 = series.quantile(0.25)

    # 3사분위수(Q3): 데이터의 하위 75% 지점 값
    Q3 = series.quantile(0.75)

    # IQR은 가운데 50% 데이터가 퍼져 있는 폭입니다.
    # Q3 - Q1로 계산합니다.
    IQR = Q3 - Q1

    # 일반적으로 Q1 - 1.5*IQR 보다 작거나
    # Q3 + 1.5*IQR 보다 큰 값을 이상치로 판단합니다.
    lower_bound = Q1 - 1.5 * IQR
    upper_bound = Q3 + 1.5 * IQR

    # clip은 범위를 벗어난 값을 삭제하지 않고,
    # 경계값까지만 잘라서 조정합니다.
    #
    # 예를 들어:
    # - 너무 작은 값은 lower_bound로 올리고
    # - 너무 큰 값은 upper_bound로 내립니다.
    return series.clip(lower=lower_bound, upper=upper_bound)


# ==========================================================
# 8. 숫자형 센서 컬럼만 선택해서 이상치 보정 적용
# ==========================================================
# 이제 방금 만든 함수를 실제 센서 컬럼들에 적용합니다.
#
# select_dtypes(include=[np.number])는 숫자형 컬럼만 고릅니다.
# 그런데 이 안에는 정답 라벨인 Pass/Fail도 포함될 수 있습니다.
# 라벨은 센서값이 아니므로 이상치 보정 대상에서 제외해야 합니다.

sensor_cols = df.select_dtypes(include=[np.number]).columns.drop("Pass/Fail")

# apply(handle_outliers)는 선택된 각 컬럼에 대해
# handle_outliers 함수를 하나씩 적용하라는 의미입니다.
# 즉, 센서 컬럼마다 IQR 기준으로 값 범위를 정리하게 됩니다.
df[sensor_cols] = df[sensor_cols].apply(handle_outliers)

print("5. 정확성 확보: 시간 형식 변환 및 IQR 기반 센서 이상치 보정 완료")


# ==========================================================
# 9. 최종 결과 확인
# ==========================================================
# 전처리가 끝났으면 결과를 확인해야 합니다.
# 여기서는 다음 두 가지를 점검합니다.
#
# 1. 최종 데이터 크기
# 2. 남아 있는 결측치 총합
#
# 결측치 총합이 0이라면, 이번 전처리 기준에서는 빈값이 모두 처리된 것입니다.

print("\n" + "=" * 30)
print(f"최종 정제 데이터 크기: {df.shape}")
print(f"결측치 총합: {df.isnull().sum().sum()}")
print("=" * 30)


# ==========================================================
# 10. 정제 데이터 저장
# ==========================================================
# 마지막으로 전처리된 결과를 새 CSV 파일로 저장합니다.
# 원본 데이터를 덮어쓰지 않고 별도 파일로 저장하는 것이 안전합니다.

df.to_csv(OUTPUT_PATH, index=False)

print(f"정제된 파일 저장 완료: {OUTPUT_PATH}")


