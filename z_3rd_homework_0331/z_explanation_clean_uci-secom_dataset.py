"""
z_explanation_clean_uci-secom_dataset.py

이 파일은 clean_uci-secom_dataset.py의 "설명용 버전"입니다.
원본 코드와 같은 전처리 흐름을 유지하면서,
초심자도 이해하기 쉽도록 코드 중간중간에 자세한 주석을 추가했습니다.

[이 파일의 목적]
- 어떤 데이터를 읽는지 이해하기
- 각 전처리 단계가 왜 필요한지 이해하기
- 함수가 어떤 입력을 받아 어떤 결과를 내는지 이해하기
- 실제로 실행도 가능한 해설용 예제 파일로 사용하기
"""

import pandas as pd
import numpy as np
from pathlib import Path

# [설명 1]
# Path는 "파일 경로"를 다루기 쉽게 도와주는 파이썬 표준 라이브러리입니다.
# 문자열로 경로를 직접 이어 붙이는 것보다 안전하고 읽기 쉽습니다.
#
# __file__:
# - 현재 실행 중인 파이썬 파일의 경로를 의미합니다.
#
# resolve():
# - 상대경로를 절대경로로 바꾸고, 실제 파일 위치를 명확히 찾습니다.
#
# parent:
# - 현재 파일이 들어 있는 "폴더"를 의미합니다.
#
# 즉, BASE_DIR은 "이 설명 파일이 저장된 폴더 경로"가 됩니다.
BASE_DIR = Path(__file__).resolve().parent

# [설명 2]
# 입력 CSV 파일과 출력 CSV 파일의 전체 경로를 미리 만들어 둡니다.
# 이렇게 하면 터미널에서 어느 위치에서 실행하든 같은 파일을 정확히 찾을 수 있습니다.
INPUT_PATH = BASE_DIR / 'uci-secom.csv'
OUTPUT_PATH = BASE_DIR / 'cleaned_secom_data.csv'


# ==========================================================
# 1. 데이터 불러오기
# ==========================================================
# [설명 3]
# pd.read_csv(...)는 CSV 파일을 읽어 pandas의 DataFrame으로 만듭니다.
#
# DataFrame은 엑셀 표처럼
# - 행(row): 데이터 한 건
# - 열(column): 변수(특성)
# 구조로 이루어진 자료형입니다.
df = pd.read_csv(INPUT_PATH)

# [설명 4]
# shape는 (행 개수, 열 개수) 형태의 튜플을 반환합니다.
# 예: (1567, 592) 라면
# - 행 1567개
# - 열 592개
# 라는 뜻입니다.
print(f"원본 데이터 크기: {df.shape}")


# ==========================================================
# 2. 유일성(Unqueness): 중복 행 제거
# ==========================================================
# [설명 5]
# 유일성은 "같은 데이터가 중복 저장되어 있지 않은가?"를 보는 품질 기준입니다.
# 완전히 똑같은 행이 여러 번 있으면 분석 결과가 왜곡될 수 있습니다.

# [설명 6]
# len(df)는 전체 행 개수를 의미합니다.
# 중복 제거 전/후 행 수를 비교하면 몇 건이 제거되었는지 쉽게 알 수 있습니다.
before_rows = len(df)

# [설명 7]
# drop_duplicates()는 완전히 같은 행을 하나만 남기고 제거합니다.
# 별도 subset을 지정하지 않았기 때문에 "모든 컬럼 값이 같은 행"을 중복으로 판단합니다.
df = df.drop_duplicates()

after_rows = len(df)
print(f"1. 유일성 확보: 중복 행 {before_rows - after_rows}건 제거")


# ==========================================================
# 3. 완전성(Completeness): 결측치 처리
# ==========================================================
# [설명 8]
# 완전성은 "데이터가 얼마나 비어 있지 않고 잘 채워져 있는가?"를 보는 기준입니다.
# 결측치가 지나치게 많은 컬럼은 분석에 방해가 될 수 있습니다.

# [설명 9]
# nan_threshold = 0.5 는 "결측 비율 50%"를 기준으로 삼겠다는 뜻입니다.
# 즉, 절반보다 더 많이 비어 있는 컬럼은 제거 대상으로 봅니다.
nan_threshold = 0.5

# [설명 10]
# isnull()은 각 셀이 결측치인지 True/False로 표시합니다.
# mean()을 열 기준으로 계산하면 True가 1, False가 0처럼 취급되어
# 각 컬럼의 "결측 비율"을 구할 수 있습니다.
#
# 예를 들어 어떤 컬럼이 100행 중 60행이 비어 있으면
# 그 컬럼의 값은 0.6이 됩니다.
null_counts = df.isnull().mean()

# [설명 11]
# 결측 비율이 0.5보다 큰 컬럼 이름만 골라냅니다.
# 결과는 Index 형태이며, 나중에 drop(columns=...)에 바로 사용할 수 있습니다.
cols_to_drop = null_counts[null_counts > nan_threshold].index

# [설명 12]
# 결측치가 너무 많은 컬럼은 아예 제거합니다.
# 이런 컬럼은 값이 너무 비어 있어서 보간해도 신뢰도가 낮을 수 있습니다.
df = df.drop(columns=cols_to_drop)

# [설명 13]
# 남아 있는 결측치는 중앙값(median)으로 채웁니다.
#
# 왜 평균(mean) 대신 중앙값(median)을 쓰는가?
# - 제조 센서 데이터에는 이상치가 있을 수 있습니다.
# - 평균은 극단값의 영향을 크게 받습니다.
# - 중앙값은 이상치 영향이 상대적으로 적어 더 안정적입니다.
#
# numeric_only=True:
# - 숫자형 컬럼만 대상으로 중앙값을 계산하라는 뜻입니다.
# - 문자열이나 시간 문자열이 섞여 있어도 오류를 줄여줍니다.
df = df.fillna(df.median(numeric_only=True))

print(f"2. 완전성 확보: 결측치 과다 컬럼 {len(cols_to_drop)}개 삭제 및 나머지 중앙값 보간")


# ==========================================================
# 4. 유효성(Validity): 상수 컬럼 제거
# ==========================================================
# [설명 14]
# 유효성은 "분석에 의미 있는 값인가?"를 보는 관점으로 이해할 수 있습니다.
# 모든 행에서 값이 똑같은 컬럼은 변별력이 없으므로 모델링에 거의 도움이 되지 않습니다.

# [설명 15]
# nunique()는 각 컬럼의 고유값 개수를 셉니다.
# 고유값 개수가 1 이하라는 것은:
# - 모든 값이 동일하거나
# - 사실상 정보가 없는 컬럼이라는 뜻입니다.
constant_cols = [col for col in df.columns if df[col].nunique() <= 1]

# [설명 16]
# 이런 상수 컬럼을 제거하면
# - 데이터 차원이 줄어들고
# - 분석 효율이 좋아지며
# - 불필요한 특성을 줄일 수 있습니다.
df = df.drop(columns=constant_cols)

print(f"3. 유효성 확보: 변동성 없는 상수 센서 {len(constant_cols)}개 제거")


# ==========================================================
# 5. 일관성(Consistency): 라벨 형식 통일
# ==========================================================
# [설명 17]
# 원본 데이터의 'Pass/Fail' 컬럼은 -1, 1 형태로 되어 있습니다.
# 이를 머신러닝에서 자주 쓰는 0, 1 형식으로 바꿉니다.
#
# 여기서는 다음과 같이 해석합니다.
# -1 -> 0 : 양품
#  1 -> 1 : 불량

# [설명 18]
# replace(-1, 0):
# - 값이 -1인 항목만 0으로 바꿉니다.
#
# astype(int):
# - 결과 타입을 정수형으로 통일합니다.
# - 모델 학습이나 통계 처리 시 더 깔끔한 형식이 됩니다.
df['Pass/Fail'] = df['Pass/Fail'].replace(-1, 0).astype(int)

print("4. 일관성 확보: 라벨 포맷 변경 (-1, 1) -> (0, 1)")


# ==========================================================
# 6. 정확성(Accuracy): 시간 변환과 이상치 보정
# ==========================================================
# [설명 19]
# 'Time' 컬럼은 처음에는 문자열처럼 읽혔을 가능성이 큽니다.
# 이를 datetime 형식으로 바꾸면
# - 날짜/시간 기준 정렬
# - 시계열 분석
# - 기간 계산
# 같은 작업이 쉬워집니다.
df['Time'] = pd.to_datetime(df['Time'])


# [설명 20]
# 아래 함수는 하나의 숫자형 컬럼(Series)에 대해
# IQR 방식으로 이상치를 잘라내는(clip) 역할을 합니다.
#
# 함수란?
# - 특정 작업을 묶어서 재사용할 수 있게 만든 코드 블록입니다.
# - 여기서는 "한 컬럼의 이상치를 처리하는 작업"을 함수로 만든 것입니다.
def handle_outliers(series):
    """
    하나의 숫자형 pandas Series를 받아,
    IQR(사분위 범위) 기준으로 지나치게 작은 값/큰 값을 경계값 안으로 잘라냅니다.

    매개변수
    - series: pandas Series
      예: 하나의 센서 컬럼 전체 값

    반환값
    - clip이 적용된 pandas Series
    """

    # [설명 21]
    # quantile(0.25)는 1사분위수(Q1), 즉 하위 25% 지점 값을 구합니다.
    Q1 = series.quantile(0.25)

    # [설명 22]
    # quantile(0.75)는 3사분위수(Q3), 즉 하위 75% 지점 값을 구합니다.
    Q3 = series.quantile(0.75)

    # [설명 23]
    # IQR = Q3 - Q1
    # 데이터의 가운데 50%가 퍼져 있는 범위를 뜻합니다.
    # 이상치를 판단할 때 자주 쓰는 대표적인 통계 기준입니다.
    IQR = Q3 - Q1

    # [설명 24]
    # 일반적으로 아래 범위를 벗어나면 이상치로 간주합니다.
    # lower_bound = Q1 - 1.5 * IQR
    # upper_bound = Q3 + 1.5 * IQR
    lower_bound = Q1 - 1.5 * IQR
    upper_bound = Q3 + 1.5 * IQR

    # [설명 25]
    # clip(lower=..., upper=...)은 값을 삭제하지 않고
    # 범위를 벗어난 값만 경계값으로 잘라서 조정합니다.
    #
    # 예:
    # - 너무 작은 값은 lower_bound로 올리고
    # - 너무 큰 값은 upper_bound로 내립니다.
    #
    # 즉, 행을 제거하지 않고 값만 완화하는 방식입니다.
    return series.clip(lower=lower_bound, upper=upper_bound)


# [설명 26]
# select_dtypes(include=[np.number])는 숫자형 컬럼만 선택합니다.
# 여기에는 센서값뿐 아니라 라벨(Pass/Fail)도 포함될 수 있습니다.
#
# 그런데 라벨은 이상치 보정을 하면 안 되므로 drop('Pass/Fail')로 제외합니다.
# 시간 컬럼 Time은 datetime 타입이므로 원래 숫자형 선택 대상에 들어가지 않습니다.
sensor_cols = df.select_dtypes(include=[np.number]).columns.drop('Pass/Fail')

# [설명 27]
# df[sensor_cols]는 선택된 숫자형 센서 컬럼들만 가져옵니다.
# apply(handle_outliers)는 각 컬럼에 handle_outliers 함수를 하나씩 적용합니다.
#
# 즉, 센서 컬럼마다:
# - Q1 계산
# - Q3 계산
# - IQR 계산
# - 경계값 계산
# - clip 적용
# 을 반복하게 됩니다.
df[sensor_cols] = df[sensor_cols].apply(handle_outliers)

print("5. 정확성 확보: 시간 형식 변환 및 IQR 기반 센서 이상치 보정 완료")


# ==========================================================
# 7. 최종 결과 확인
# ==========================================================
# [설명 28]
# 전처리 후 데이터의 크기와 남아 있는 결측치 수를 확인합니다.
# 이 단계는 정제 결과를 빠르게 검산하는 용도입니다.
print("\n" + "=" * 30)
print(f"최종 정제 데이터 크기: {df.shape}")
print(f"결측치 총합: {df.isnull().sum().sum()}")
print("=" * 30)


# ==========================================================
# 8. 정제된 데이터 저장
# ==========================================================
# [설명 29]
# to_csv(..., index=False)는 DataFrame을 CSV 파일로 저장합니다.
#
# index=False를 넣는 이유:
# - pandas의 기본 행 번호(0, 1, 2, ...)를
#   별도 컬럼으로 저장하지 않기 위해서입니다.
df.to_csv(OUTPUT_PATH, index=False)

print(f"정제된 파일 저장 완료: {OUTPUT_PATH}")


